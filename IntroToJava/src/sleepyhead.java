import javax.swing.JOptionPane;

public class sleepyhead {

	public static void main(String[] args) {
		
		boolean isWeekday, isVacation;
		int day = JOptionPane.showConfirmDialog(null, "Is it a weekend?", "", JOptionPane.YES_NO_OPTION);
		if (day == JOptionPane.YES_OPTION)
		{
			JOptionPane.showMessageDialog(null, "What are you doing?! Get back to sleep!!!");
		}
		
		if (day == JOptionPane.NO_OPTION)
		{
			JOptionPane.showMessageDialog(null, "What are you doing?! Go to work!!!");
		}
	

		/*
		 * Print “sleep in” if it is a vacation or a weekend. If it’s a weekday,
		 * print “get up lazybones!” If it is a weekday, and we are on vacation,
		 * print “sleep in”.
		 */
		
	}
}

